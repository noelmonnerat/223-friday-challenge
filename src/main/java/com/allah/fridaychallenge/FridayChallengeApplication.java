package com.allah.fridaychallenge.car;

public class Car {
	private String uuid;
	private String numberPlate;
	private String brand;
	private String model;

	public Car(String uuid, String numberPlate, String brand, String model) {
		this.uuid = uuid;
		this.numberPlate = numberPlate;
		this.brand = brand;
		this.model = model;
	}
}
